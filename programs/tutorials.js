window.findCoachBertinTutorial = function(name){
  var key = String(name||"").replace(/^[A-Z][0-9]?\.\s*/,"").trim();
  var db = window.COACH_BERTIN_TUTORIALS || {};
  if(db[key]) return {key:key, item:db[key]};
  var lower = key.toLowerCase();
  var found = Object.keys(db).find(function(k){ return k.toLowerCase() === lower; });
  if(found) return {key:found, item:db[found]};
  found = Object.keys(db).find(function(k){
    var lk=k.toLowerCase();
    return lower.indexOf(lk)>=0 || lk.indexOf(lower)>=0;
  });
  return found ? {key:found, item:db[found]} : null;
};
